def doc(key: str, pages: list[int]) -> dict:
    did, fn, _ = DOCS[key]
    return {"doc_id": did, "filename": fn, "page_numbers": pages}


# Running fact-id counter used by the fact() helper.
_fact_counter = [0]


def fact(text: str, doc_key: str) -> dict:
    _fact_counter[0] += 1
    fid = f"acmf_f{_fact_counter[0]:04d}"
    return {"fact_id": fid, "text": text, "doc_id": DOCS[doc_key][0]}


def item(
    qid: str, query: str, answer: str, cats: list[str], diff: str,
    facts: list[dict], docs: list[dict], label: str = "answerable", **meta,
) -> dict:
    return {
        "query_id": qid,
        "query": query,
        "gt_answer": answer,
        "categories": cats,
        "difficulty": diff,
        "eval_label": label,
        "gt_supporting_facts": facts,
        "gt_documents": docs,
        "metadata": {"language": "en", **meta},
    }


