    assert len(qids) == len(set(qids)), f"duplicate query_id: {sorted({q for q in qids if qids.count(q) > 1})}"

    (OUT_DIR / "dataset.json").write_text(
        json.dumps(DATASET, indent=2, ensure_ascii=False), encoding="utf-8"
    )
    (OUT_DIR / "leaderboard.json").write_text(
        json.dumps(LEADERBOARD_FILE, indent=2, ensure_ascii=False), encoding="utf-8"
    )
    (OUT_DIR / "corpus.json").write_text(
        json.dumps(CORPUS, indent=2, ensure_ascii=False), encoding="utf-8"
    )

