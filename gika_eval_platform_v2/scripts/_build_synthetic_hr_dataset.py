def doc(key: str, pages: list[int]) -> dict:
    did, fn, _ = DOCS[key]
    return {"doc_id": did, "filename": fn, "page_numbers": pages}


_fact_counter = [0]


def fact(text: str, doc_key: str) -> dict:
    _fact_counter[0] += 1
    fid = f"nvhr_f{_fact_counter[0]:04d}"
    return {"fact_id": fid, "text": text, "doc_id": DOCS[doc_key][0]}


def item(qid, query, answer, cats, diff, facts, docs, label="answerable", **meta):
    return {
        "query_id": qid,
        "query": query,
        "gt_answer": answer,
        "categories": cats,
        "difficulty": diff,
        "eval_label": label,
        "gt_supporting_facts": facts,
        "gt_documents": docs,
        "metadata": {"language": "en", **meta},
    }


